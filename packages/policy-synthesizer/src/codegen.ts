// Install script generator
// ---------------------------------------------------------------------------

export function generateInstallScript(
  contextRuleLabel: string,
  scope: Array<{ contractId: string; functionName: string }>,
  lifetimeSecs: number,
  standardPolicies: Array<{ kind: string; config: unknown }>,
  customPolicyAddresses: string[],
): string {
  return `/**
 * Install Script — ${contextRuleLabel}
 * GENERATED BY OZ POLICY BUILDER — REVIEW BEFORE RUNNING.
 *
 * Steps:
 *   1. Review the generated policy Rust source files above.
 *   2. Deploy any custom policy contracts and fill in their addresses below.
 *   3. Run: ts-node install.ts --account <your-smart-account-id> --network testnet
 */

import { buildAddContextRuleTx, buildPolicyExecuteTx, submitViaRelayer } from '@oz-policy-builder/policy-synthesizer/wallet';
import { Address, xdr } from '@stellar/stellar-sdk';

// TODO: fill in deployed custom policy contract addresses
const CUSTOM_POLICY_ADDRESSES: string[] = [
${customPolicyAddresses.map((a) => `  '${a}',`).join('\n')}
];

const CONTEXT_RULE_LABEL = '${contextRuleLabel}';
const LIFETIME_SECONDS = ${lifetimeSecs};
const SCOPE = ${JSON.stringify(scope, null, 2)};

async function install(smartAccountId: string, networkPassphrase: string) {
  const policyAddresses: string[] = [
    // Standard OZ policies (already deployed):
    ${standardPolicies.map((p) => `'<OZ_${p.kind.toUpperCase()}_ADDRESS>',`).join('\n    ')}
    // Custom policies (fill in after deploying):
    ...CUSTOM_POLICY_ADDRESSES,
  ];

  // Build install params for each custom policy using the typed struct approach.
  // Each policy is installed via add_policy(rule_id, policy_addr, encoded_params).
  const installParamsXdr = buildTimeBoundParamsXdr(/* fill: start_ledger, end_ledger */);

  const { hostFuncXdr, ephemeralSigner } = await buildAddContextRuleTx({
    walletContractId: smartAccountId,
    ruleName: CONTEXT_RULE_LABEL,
    targetContract: SCOPE[0]?.contractId ?? '',
    policyAddr: policyAddresses[0] ?? '',
    installParamsXdr,
    networkPassphrase,
  });

  const txHash = await submitViaRelayer(hostFuncXdr, ephemeralSigner.publicKey());
  console.log('Context rule installed. TX hash:', txHash);
}

function buildTimeBoundParamsXdr(startLedger: number, endLedger: number): string {
  // Encode TimeBoundParams as XDR ScVal struct for add_policy
  const params = xdr.ScVal.scvMap([
    new xdr.ScMapEntry({
      key: xdr.ScVal.scvSymbol('start_ledger'),
      val: xdr.ScVal.scvU32(startLedger),
    }),
    new xdr.ScMapEntry({
      key: xdr.ScVal.scvSymbol('end_ledger'),
      val: xdr.ScVal.scvU32(endLedger),
    }),
  ]);
  return params.toXDR('base64');
}

const [,, accountId, network = 'testnet'] = process.argv;
if (!accountId) {
  console.error('Usage: ts-node install.ts <smart-account-id> [network]');
  process.exit(1);
}

const PASSPHRASES: Record<string, string> = {
  mainnet: 'Public Global Stellar Network ; September 2015',
  testnet: 'Test SDF Network ; September 2015',
};

install(accountId, PASSPHRASES[network] ?? PASSPHRASES.testnet).catch(console.error);
`;
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function toCamelCase(snakeCase: string): string {
  return snakeCase
    .split('_')
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join('');
}

function sanitizeForComment(s: string): string {
  return s.replace(/[\r\n\t]/g, ' ').replace(/[^\x20-\x7E]/g, '?');
}
