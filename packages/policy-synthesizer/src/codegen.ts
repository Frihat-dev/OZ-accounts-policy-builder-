/**
 * Rust code generation for custom Soroban policy contracts.
 *
 * All generated contracts use the real OZ Accounts policy interface:
 *   install(env, install_params, context_rule: ContextRule, smart_account: Address)
 *   enforce(env, context: Context, signers: Vec<Signer>, context_rule, smart_account)
 *   uninstall(env, context_rule, smart_account)
 *   can_enforce(...) -> bool       — read-only pre-check
 *
 * DataKey uses u32 (context_rule.id), not BytesN<32>.
 * No require_auth in install/uninstall — OZ smart account handles it.
 * Error handling via policy_panic(&env, PolicyError::X).
 */

import type { PolicySpec, RustPolicySource } from './types.js';
import type { TimeBoundConfig, CallFilterConfig, FrequencyLimitConfig } from './types.js';

// ---------------------------------------------------------------------------
// Time-Bound policy generator
// ---------------------------------------------------------------------------

export function generateTimeBoundSource(spec: PolicySpec): RustPolicySource {
  const config = spec.config as TimeBoundConfig;
  const name = spec.contractName ?? 'policy_time_bound';

  const source = `//! Generated Time-Bound Policy
//! Start ledger: ${config.startLedger}
//! End ledger:   ${config.endLedger}
//!
//! Allows invocations only when the current ledger sequence is within
//! [start_ledger, end_ledger] (inclusive).
//!
//! GENERATED BY OZ POLICY BUILDER — REVIEW BEFORE DEPLOYING.

#![no_std]

use oz_policy_trait::{policy_panic, ContextRule, PolicyError, Signer};
use soroban_sdk::{auth::Context, contract, contractimpl, contracttype, Address, Env, Vec};

// ── Params ────────────────────────────────────────────────────────────────────

#[contracttype]
pub struct TimeBoundParams {
    pub start_ledger: u32,
    pub end_ledger: u32,
}

// ── Storage ───────────────────────────────────────────────────────────────────

#[contracttype]
pub struct TimeBoundConfig {
    pub start_ledger: u32,
    pub end_ledger: u32,
}

#[contracttype]
pub enum DataKey {
    Config(Address, u32),
}

// ── Contract ──────────────────────────────────────────────────────────────────

#[contract]
pub struct ${toCamelCase(name)};

#[contractimpl]
impl ${toCamelCase(name)} {
    pub fn install(
        env: Env,
        install_params: TimeBoundParams,
        context_rule: ContextRule,
        smart_account: Address,
    ) {
        if install_params.start_ledger > install_params.end_ledger {
            policy_panic(&env, PolicyError::InvalidConfig);
        }
        let cfg = TimeBoundConfig {
            start_ledger: install_params.start_ledger,
            end_ledger: install_params.end_ledger,
        };
        env.storage()
            .persistent()
            .set(&DataKey::Config(smart_account, context_rule.id), &cfg);
    }

    pub fn enforce(
        env: Env,
        _context: Context,
        _authenticated_signers: Vec<Signer>,
        context_rule: ContextRule,
        smart_account: Address,
    ) {
        let cfg: TimeBoundConfig = env
            .storage()
            .persistent()
            .get(&DataKey::Config(smart_account, context_rule.id))
            .unwrap_or_else(|| policy_panic(&env, PolicyError::NotInstalled));

        let seq = env.ledger().sequence();
        if seq < cfg.start_ledger || seq > cfg.end_ledger {
            policy_panic(&env, PolicyError::TimeWindowViolation);
        }
    }

    pub fn uninstall(env: Env, context_rule: ContextRule, smart_account: Address) {
        env.storage()
            .persistent()
            .remove(&DataKey::Config(smart_account, context_rule.id));
    }

    pub fn can_enforce(env: Env, context_rule: ContextRule, smart_account: Address) -> bool {
        let cfg: TimeBoundConfig = match env
            .storage()
            .persistent()
            .get(&DataKey::Config(smart_account, context_rule.id))
        {
            Some(c) => c,
            None => return false,
        };
        let seq = env.ledger().sequence();
        seq >= cfg.start_ledger && seq <= cfg.end_ledger
    }

    pub fn get_config(env: Env, context_rule_id: u32, smart_account: Address) -> TimeBoundConfig {
        env.storage()
            .persistent()
            .get(&DataKey::Config(smart_account, context_rule_id))
            .unwrap_or_else(|| policy_panic(&env, PolicyError::NotInstalled))
    }
}
`;

  return {
    contractName: name,
    filename: `${name}/src/lib.rs`,
    source,
    cargoToml: generateCargoToml(name),
  };
}

// ---------------------------------------------------------------------------
// Call-Filter policy generator
// ---------------------------------------------------------------------------

export function generateCallFilterSource(spec: PolicySpec): RustPolicySource {
  const config = spec.config as CallFilterConfig;
  const name = spec.contractName ?? 'policy_call_filter';

  const source = `//! Generated Call Filter Policy
//!
//! Allowed calls:
${config.allowedCalls.map((c) => `//!   - ${sanitizeForComment(c.contractId)}::${sanitizeForComment(c.functionName)}`).join('\n')}
//!
//! Supports typed argument constraints:
//!   ExactAddress(pos, addr)  — arg at position must equal address
//!   ExactValue(pos, val)     — arg at position must equal i128
//!   AmountMax(pos, max)      — arg must be <= max
//!   AmountMin(pos, min)      — arg must be >= min
//!
//! GENERATED BY OZ POLICY BUILDER — REVIEW BEFORE DEPLOYING.

#![no_std]

use oz_policy_trait::{policy_panic, ContextRule, PolicyError, Signer};
use soroban_sdk::{
    auth::{Context, ContractContext},
    contract, contractimpl, contracttype, Address, Env, TryFromVal, Vec,
};

// ── Types ─────────────────────────────────────────────────────────────────────

#[contracttype]
pub enum ArgConstraint {
    ExactAddress(u32, Address),
    ExactValue(u32, i128),
    AmountMax(u32, i128),
    AmountMin(u32, i128),
}

#[contracttype]
pub struct AllowedCall {
    pub contract: Address,
    pub fn_name: soroban_sdk::Symbol,
    pub arg_constraints: Vec<ArgConstraint>,
}

#[contracttype]
pub struct CallFilterParams {
    pub allowed_calls: Vec<AllowedCall>,
}

#[contracttype]
pub struct CallFilterConfig {
    pub allowed_calls: Vec<AllowedCall>,
}

#[contracttype]
pub enum DataKey {
    Config(Address, u32),
}

// ── Contract ──────────────────────────────────────────────────────────────────

#[contract]
pub struct ${toCamelCase(name)};

#[contractimpl]
impl ${toCamelCase(name)} {
    pub fn install(
        env: Env,
        install_params: CallFilterParams,
        context_rule: ContextRule,
        smart_account: Address,
    ) {
        if install_params.allowed_calls.is_empty() {
            policy_panic(&env, PolicyError::InvalidConfig);
        }
        let cfg = CallFilterConfig { allowed_calls: install_params.allowed_calls };
        env.storage()
            .persistent()
            .set(&DataKey::Config(smart_account, context_rule.id), &cfg);
    }

    pub fn enforce(
        env: Env,
        context: Context,
        _authenticated_signers: Vec<Signer>,
        context_rule: ContextRule,
        smart_account: Address,
    ) {
        let cfg: CallFilterConfig = env
            .storage()
            .persistent()
            .get(&DataKey::Config(smart_account, context_rule.id))
            .unwrap_or_else(|| policy_panic(&env, PolicyError::NotInstalled));

        if !is_allowed(&env, &cfg, &context) {
            policy_panic(&env, PolicyError::ScopeViolation);
        }
    }

    pub fn uninstall(env: Env, context_rule: ContextRule, smart_account: Address) {
        env.storage()
            .persistent()
            .remove(&DataKey::Config(smart_account, context_rule.id));
    }

    pub fn can_enforce(
        env: Env,
        context: Context,
        context_rule: ContextRule,
        smart_account: Address,
    ) -> bool {
        let cfg: CallFilterConfig = match env
            .storage()
            .persistent()
            .get(&DataKey::Config(smart_account, context_rule.id))
        {
            Some(c) => c,
            None => return false,
        };
        is_allowed(&env, &cfg, &context)
    }

    pub fn get_config(env: Env, context_rule_id: u32, smart_account: Address) -> CallFilterConfig {
        env.storage()
            .persistent()
            .get(&DataKey::Config(smart_account, context_rule_id))
            .unwrap_or_else(|| policy_panic(&env, PolicyError::NotInstalled))
    }
}

// ── Helpers ───────────────────────────────────────────────────────────────────

fn is_allowed(env: &Env, cfg: &CallFilterConfig, context: &Context) -> bool {
    let ctx: &ContractContext = match context {
        Context::Contract(c) => c,
        _ => return false,
    };
    for i in 0..cfg.allowed_calls.len() {
        let allowed = cfg.allowed_calls.get(i).unwrap();
        if allowed.contract == ctx.contract && allowed.fn_name == ctx.fn_name {
            if constraints_pass(env, &allowed.arg_constraints, ctx) {
                return true;
            }
        }
    }
    false
}

fn constraints_pass(env: &Env, constraints: &Vec<ArgConstraint>, ctx: &ContractContext) -> bool {
    for i in 0..constraints.len() {
        match constraints.get(i).unwrap() {
            ArgConstraint::ExactAddress(pos, required) => {
                let v = match ctx.args.get(pos) { Some(v) => v, None => return false };
                match Address::try_from_val(env, &v) {
                    Ok(a) if a == required => {}
                    _ => return false,
                }
            }
            ArgConstraint::ExactValue(pos, required) => {
                let v = match ctx.args.get(pos) { Some(v) => v, None => return false };
                match i128::try_from_val(env, &v) {
                    Ok(n) if n == required => {}
                    _ => return false,
                }
            }
            ArgConstraint::AmountMax(pos, max) => {
                let v = match ctx.args.get(pos) { Some(v) => v, None => return false };
                match i128::try_from_val(env, &v) {
                    Ok(n) if n <= max => {}
                    _ => return false,
                }
            }
            ArgConstraint::AmountMin(pos, min) => {
                let v = match ctx.args.get(pos) { Some(v) => v, None => return false };
                match i128::try_from_val(env, &v) {
                    Ok(n) if n >= min => {}
                    _ => return false,
                }
            }
        }
    }
    true
}
`;

  return {
    contractName: name,
    filename: `${name}/src/lib.rs`,
    source,
    cargoToml: generateCargoToml(name),
  };
}

